class literals{
    public static void main(String a[]){

        int bin = 0b1001;
        int hex = 0xFF;

        int zeros = 1_00_00_00_000;

        double exp = 12e10;     //or 12E10 same

        char alph = 'a';
        alph++;

        System.out.println(bin);
        System.out.println(hex);
        System.out.println(zeros);
        System.out.println(exp);
        System.out.println(alph);
        
    }
}